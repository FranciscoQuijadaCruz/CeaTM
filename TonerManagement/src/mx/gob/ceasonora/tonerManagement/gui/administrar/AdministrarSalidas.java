    private void reporteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reporteButtonActionPerformed

        try {
            salida = salidas.get(salidasTable.getSelectedRow());
                JDialog espera = new JDialog();
                espera.setLocationRelativeTo(null);
                espera.setTitle("Generando reporte...");
                espera.pack();
                espera.setVisible(true);

                HashMap parametros = new HashMap();

                parametros.put("idSalida", salida.getId());

                InstanciaConexion conexion = InstanciaConexion.getInstanciaConexion();
                conexion.conexion();
                JasperPrint jp = JasperFillManager.fillReport("src\\reporte\\reporte_salida.jasper", parametros, conexion.con);
                JasperViewer.viewReport(jp, false);
                espera.setVisible(false);

            } catch (Exception e) {
                e.printStackTrace();
            }
        
    }//GEN-LAST:event_reporteButtonActionPerformed


