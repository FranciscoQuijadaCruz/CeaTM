    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed

        try {
            herramienta.setCantidadTotal(Integer.parseInt(cantidadTotalTextField.getText()));
            herramienta.setCantidadDisponible(Integer.parseInt(cantidadDisponibleTextField.getText()));
            herramienta.setNombre(nombreTextField.getText());
            herramienta.setPrecioUnitario(Double.parseDouble(precioUnitarioTextField.getText()));
            herramienta.setDescripcion(descripcionTextField.getText());
            herramienta.setCodigoProducto(codigoProductoTextField.getText());
            HerramientaInt herImp = new HerramientaImp();
            if(herImp.Editar(herramienta, herramienta)){
                JOptionPane.showMessageDialog(null, "Datos acutalizados con éxito.", "Cambios guardados", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }

        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_guardarButtonActionPerformed

