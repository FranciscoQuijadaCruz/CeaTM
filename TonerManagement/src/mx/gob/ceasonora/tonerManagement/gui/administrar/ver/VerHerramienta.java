    private void cantidadTotalTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cantidadTotalTextFieldKeyReleased

        if(cantidadTotalTextField.getText().equals("")){
            cantidadTotalTextField.setText("0");
        }
        if(cantidadTotalTextField.getText().startsWith("0")&&cantidadTotalTextField.getText().length()>1){
            cantidadTotalTextField.setText(cantidadTotalTextField.getText().substring(1, cantidadTotalTextField.getText().length()));
        }
        int cantidadTotalNueva = Integer.parseInt(cantidadTotalTextField.getText());
        int sumar = cantidadTotalNueva-cantidadTotal;
        cantidadTotal = cantidadTotalNueva;
        int cantidadDisponible = Integer.parseInt(cantidadDisponibleTextField.getText());
        int cantidadDisponibleNueva = cantidadDisponible + sumar;
        cantidadDisponible = cantidadDisponibleNueva;
        cantidadDisponibleTextField.setText(String.valueOf(cantidadDisponibleNueva));
        
    }//GEN-LAST:event_cantidadTotalTextFieldKeyReleased


