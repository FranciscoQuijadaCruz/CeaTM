    private void reporteFaltantesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reporteFaltantesButtonActionPerformed

        try {
            JDialog espera = new JDialog();
            espera.setLocationRelativeTo(null);
            espera.setTitle("Generando reporte...");
            espera.pack();
            espera.setVisible(true);
            
            conexion.conexion();
            JasperPrint jp = JasperFillManager.fillReport("src\\reporte\\reporte_consumible_faltantes.jasper", null, conexion.con);
            JasperViewer.viewReport(jp, false);
            espera.setVisible(false);

        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_reporteFaltantesButtonActionPerformed

