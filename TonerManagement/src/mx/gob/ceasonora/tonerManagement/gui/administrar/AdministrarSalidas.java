    private void verButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verButtonActionPerformed

        if(!salidasTable.getSelectionModel().isSelectionEmpty()){
            salida = salidas.get(salidasTable.getSelectedRow());
            VerSalida verSalida = new VerSalida();
            JDesktopPane escritorio = getDesktopPane();
            if(!escritorio.isAncestorOf(verSalida)){
                escritorio.add(verSalida);
                verSalida.setVisible(true);
            }else{
                escritorio.setSelectedFrame(verSalida);
            }
        }
        
    }//GEN-LAST:event_verButtonActionPerformed

    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed

        if(!salidasTable.getSelectionModel().isSelectionEmpty()){
            salida = salidas.get(salidasTable.getSelectedRow());
            if(JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea eliminar la Salida "+salida.getId()+"?", "Eliminar Salida", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE)==0){
                try {
                    if(salImp.Eliminar(salida)){
                        JOptionPane.showMessageDialog(null, "Salida eliminada con éxito", "Salida eliminada", JOptionPane.INFORMATION_MESSAGE);
                        modelo = new DefaultTableModel();
                        salidasTable.setModel(modelo);
                        modelo.setColumnIdentifiers(titulos);
                        salidas = salImp.lista_salidas();
                        for(Salida sal : salidas){
                            Object[] o = new Object[4];
                            o[0] = sal.getId();
                            o[1] = sal.getFecha();
                            o[2] = sal.getConsumible().getNombre();
                            o[3] = sal.getPersonal().getNombre();
                            
                            modelo.addRow(o);
                        }
                    }
                } catch (SQLException e) {
                    System.out.println("No se pudo eliminar: "+e.getSQLState());
                }
            }
        }
        
    }//GEN-LAST:event_eliminarButtonActionPerformed

