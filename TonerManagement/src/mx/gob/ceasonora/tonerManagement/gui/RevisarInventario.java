    public RevisarInventario() {
        initComponents();
        mostrarGroup.add(faltantesButton);
        mostrarGroup.add(escasosButton);
        
        faltantes = MenuPrincipal.faltantes;
        escasos = MenuPrincipal.escasos;
        
        modelo = new DefaultTableModel();
        consumiblesTable.setModel(modelo);
        modelo.setColumnIdentifiers(titulos);
        
        try {
            if(!faltantes.isEmpty()){
                consumibles = faltantes;
                faltantesButton.setSelected(true);
            }else if(!escasos.isEmpty()){
                consumibles = escasos;
                escasosButton.setSelected(true);
            }
            for(Consumible con: consumibles){
                Object[] o = new Object[4];
                o[0] = con.getId();
                o[1] = con.getCodigoProducto();
                o[2] = con.getNombre();
                o[3] = con.getCantidad();
                
                modelo.addRow(o);
            }
        } catch (Exception e) {
            System.out.println("Error al mostrar los datos en la tabla");
        }
    }

